function calculateMaxY(d, x) {
  return Math.sqrt(Math.pow(d, 2) - Math.pow(x, 2));
}

function solution(k, d) {
  let count = 0;

  for (let x = 0; x <= d; x += k) {
    const maxY = calculateMaxY(d, x);
    console.log(maxY);
    count += Math.floor(maxY / k) + 1;
  }

  return count;
}

